class Relational{
      public static void main(String[]args){
        char a = 'A';
        char b = 'B';
        System.out.println("A"+ "<"+"B"+"is"+(a<b) );
        System.out.println("A"+ "<="+"B"+"is"+(a<=b) );
        System.out.println("A"+ ">"+"B"+"is"+(a>b) );
        System.out.println("A"+ ">="+"B"+"is"+(a>=b) );
        System.out.println("A"+ "=="+"B"+"is"+(a==b) );
        System.out.println("A"+ "!="+"B"+"is"+(a!=b) );
      }

}